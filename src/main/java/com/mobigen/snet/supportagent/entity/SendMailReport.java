package com.mobigen.snet.supportagent.entity;

public class SendMailReport {

	private String mailCd;
	
	private String mailTitle;
	
	private int mailType;
	
	private String mailSend;
	
	private String mailNow;
	
	private String userId;
	
	private String userNm;
	
	private String userMail;
	
	private String mailData;

	public String getMailCd() {
		return mailCd;
	}

	public void setMailCd(String mailCd) {
		this.mailCd = mailCd;
	}

	public String getMailTitle() {
		return mailTitle;
	}

	public void setMailTitle(String mailTitle) {
		this.mailTitle = mailTitle;
	}

	public int getMailType() {
		return mailType;
	}

	public void setMailType(int mailType) {
		this.mailType = mailType;
	}

	public String getMailSend() {
		return mailSend;
	}

	public void setMailSend(String mailSend) {
		this.mailSend = mailSend;
	}

	public String getMailNow() {
		return mailNow;
	}

	public void setMailNow(String mailNow) {
		this.mailNow = mailNow;
	}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getUserNm() {
		return userNm;
	}

	public void setUserNm(String userNm) {
		this.userNm = userNm;
	}

	public String getUserMail() {
		return userMail;
	}

	public void setUserMail(String userMail) {
		this.userMail = userMail;
	}

	public String getMailData() {
		return mailData;
	}

	public void setMailData(String mailData) {
		this.mailData = mailData;
	}
}
